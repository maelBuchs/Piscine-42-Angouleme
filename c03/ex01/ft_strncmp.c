int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				diff;
	unsigned int	i;

	diff = 0;
	i = 0;
	while (i < n - 1 && (s1[i] == s2[i] && s1[i] != 0 && s2[i] != 0))
			i++;
	if (s1[i] != s2[i])
		diff = s1[i] - s2[i];
	if (n == 0)
		return (0);
	return (diff);
}
