#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		to_print;

	if (nb == -2147483648)
	{
		write(1, ("-2147483648"), 11);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = nb - nb - nb;
			ft_putnbr(nb);
		}
		else if (nb >= 0)
		{	
		to_print = (nb % 10) + '0';
			if (nb / 10 != 0)
			{
				ft_putnbr(nb / 10);
			}
			write(1, &to_print, 1);
		}
	}
}
