char	*ft_strupcase(char *str)
{
	int	temp;

	temp = 0;
	while (str[temp])
	{
		if (str[temp] >= 'a' && str[temp] <= 'z')
			str[temp] -= 32;
		temp++;
	}
	return (str);
}
