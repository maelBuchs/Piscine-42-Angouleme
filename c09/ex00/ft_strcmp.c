int	ft_strcmp(char *s1, char *s2)
{
	int	diff;
	int	i;

	diff = 0;
	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i])
			i++;
	if (s1[i] != s2[i])
		diff = s1[i] - s2[i];
	return (diff);
}
