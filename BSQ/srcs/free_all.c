#include "../includes/ft.h"

int	free_all(t_param_tab s1, char ***tab)
{
	int	i;

	i = 0;
	while (i < s1.length)
	{
		free((*tab)[i]);
		i++;
	}
	free((*tab));
	i = 0;
	return (0);
}
