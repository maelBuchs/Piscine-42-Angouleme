#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main(int argv, char **argc)
{
	(void) argv;
	write(1, argc[0], ft_strlen(argc[0]));
}
